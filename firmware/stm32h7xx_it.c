void EXTI3_IRQHandler( void )
{
  int timerval;
  timerval = ( int ) __HAL_TIM_GetCounter( &htim2 );
  __HAL_TIM_SetCounter( &htim2, 0x0000 ); //clear timer

  ir_remote_rx( timerval );
  HAL_GPIO_EXTI_IRQHandler( GPIO_PIN_3 );
}
